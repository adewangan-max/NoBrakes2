"use client";

import { useState } from "react";
import Link from "next/link";
import { cn } from "@/lib/utils";

const routes = [
  { name: "Home", path: "/", icon: HomeIcon },
];

export function Sidebar() {
  const [isOpen, setIsOpen] = useState(false);

  return (
    <>
      {/* Mobile Toggle Button */}
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="md:hidden fixed top-4 left-4 z-[60] p-2 rounded-md bg-white/5 border border-white/10 text-white hover:bg-white/10 transition-colors"
      >
        <MenuIcon />
      </button>

      {/* Backdrop for Mobile */}
      {isOpen && (
        <div
          className="md:hidden fixed inset-0 z-40 bg-black/50 backdrop-blur-sm"
          onClick={() => setIsOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          "fixed top-0 left-0 z-50 h-screen w-64 border-r border-white/10 bg-black/80 backdrop-blur-2xl transition-transform duration-300 ease-in-out flex flex-col",
          isOpen ? "translate-x-0" : "-translate-x-full md:translate-x-0"
        )}
      >
        <div className="h-16 flex items-center px-6 border-b border-white/10">
          <span className="text-xl font-black bg-gradient-to-r from-indigo-400 to-purple-400 bg-clip-text text-transparent font-quantico">
            SEO MASTER
          </span>
        </div>

        <nav className="flex-1 py-6 px-4 space-y-2 overflow-y-auto">
          {routes.map((route) => (
            <Link
              key={route.path}
              href={route.path}
              onClick={() => setIsOpen(false)}
              className="flex items-center gap-3 px-4 py-3 rounded-xl text-slate-300 hover:text-white hover:bg-indigo-500/10 hover:border-indigo-500/20 border border-transparent transition-all group"
            >
              <route.icon className="w-5 h-5 text-slate-400 group-hover:text-indigo-400 transition-colors" />
              <span className="font-medium text-sm">{route.name}</span>
            </Link>
          ))}
        </nav>

        <div className="p-4 border-t border-white/10">
          <div className="px-4 py-3 rounded-xl bg-white/5 border border-white/10">
            <p className="text-xs text-slate-400 text-center">
              Logged in as Admin
            </p>
          </div>
        </div>
      </aside>
    </>
  );
}

